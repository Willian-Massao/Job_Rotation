// 110 km/h + 80 km/h = 190 km/h
// 
// A distância total que eles precisam percorrer é de 100 km, e a velocidade relativa é de 190 km/h, então podemos usar a fórmula da velocidade média para calcular o tempo necessário para que eles se encontrem:
// 
// tempo = distância / velocidade
// tempo = 100 km / 190 km/h
// tempo = 0,5263 horas
// 
// No entanto, precisamos levar em consideração os pedágios como obstáculos. Como o carro possui tag de pedágio, não precisa parar em nenhum deles, enquanto o caminhão leva 5 minutos extras em cada pedágio. Existem dois pedágios no caminho, então o caminhão levará 10 minutos extras, ou 1/6 de hora.
// 
// Agora podemos calcular a distância percorrida por cada veículo no tempo que levou para se encontrarem. O carro viajou a uma velocidade constante de 110 km/h por todo o tempo, então sua distância percorrida é simplesmente a velocidade multiplicada pelo tempo:
// 
// distância do carro = 110 km/h x 0,5263 h
// distância do carro = 57,89 km
// 
// Já o caminhão viajou a 80 km/h, exceto pelos 10 minutos extras que levou nos pedágios, então sua distância percorrida será menor:
// 
// distância do caminhão = 80 km/h x (0,5263 h - 1/6 h)
// distância do caminhão = 67,37 km
// 
// Portanto, quando eles se cruzarem na estrada, o carro estará mais próximo de Ribeirão Preto, já que o caminhão viajou uma distância maior antes de se encontrar com o carro.